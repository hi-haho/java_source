package debug;

public class Calculator {
	public Calculator() {
		// 단위테스트는 짜다가 테스트하고 하다가 테스트하고!
	}

	public int plus(int a, int b) {
		return a + b;
	}

	public int minus(int a, int b) {
		return a - b;
	}

	public int multi(int a, int b) {
		return a * b;
	}

	public int divide(int a, int b) {
		return a / b;
	}
}
